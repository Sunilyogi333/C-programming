/*
a. Global varaibles
- It is declared outside and above main function.
- If not initialized default value is 0.
- Scope is global. (i.e. they can be used anywhere in the program.)
- Created before program execution and distroyed when program terminates.
- Too much use of global varaible makes program complex and difficult to understand and debug.
*/

/*
b. local varaibles
- It is declared inside a function block.
- If not initialized default value is garbage value.
- Scope is local. (i.e. they can be used only within the function in which they are declared.)
- Created when function start execution and distroyed when function terminates.
- Use of local varaible is advisable.
*/