/*
Arithmetic Operators: +, -, *, /, %, ++, --
- These operators are used to perform arithmetic/mathematical operations on operands.
- These operators can be used with all numeric types.
- The operators ++ and -- are known as increment and decrement operators respectively.
- The operators +, -, *, /, % are known as binary operators as they require two operands.
- The operators ++ and -- are known as unary operators as they require only one operand.

Example:
2 + 5 - 3 + 7 = 4 + 7 = 11
2 * 5 / 2 % 4 = 10 / 2 % 4 = 5 % 4 = 1
2 + 5 * 3 % 2 - 6 = 2 + 15 % 2 - 6 = 2 + 1 - 6 = 3 - 6 = -3
*/